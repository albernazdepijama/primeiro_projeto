# Calculadora pyton

num1 = int (input("Digite o primeiro numero: "))

num2 = int (input ("Digite o segundo numero: "))

soma = num1 + num2
print (f" A soma de {num1} e {num2} e igual a = {soma}")
subtracao = num1 - num2
print (f" A subtração de {num1} e {num2} e igual a = {subtracao}")
multiplicacao = num1 * num2
print (f" A multiplicação de {num1} e {num2} e igual a = {multiplicacao}")
divisao = num1 / num2
print (f" A divisão de {num1} e {num2} e igual a = {divisao}")
divisao_inteira = num1 // num2
print (f" A divisão inteira de {num1} e {num2} e igual a = {divisao_inteira}")
resto_divisao = num1 % num2
print (f" O resto da divisão de  {num1} e {num2} e igual a = {resto_divisao}")
potenciacao = num1 ** num2
print (f" A potrenciação de {num1} e {num2} e igual a = {potenciacao}")
