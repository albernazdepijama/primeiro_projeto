# Contador de intevalo customizado basico
valor_inicial = int(input("Digite o primneiro valor de passos: "))
valor_final = int(input("Digite o segundo valor de passos: "))
valor_de_passos = int(input("Digite o terceiro valor passos: "))

print("Resultado ")

for i in range (valor_inicial, valor_final, valor_de_passos):
    print(i)


